#include <unistd.h>

int	main(void)
{
	char	digit;
	char	temp;
	int		i;

	i = 0;
	digit = 'z';
	while (i < 26)
	{
		temp = (digit--) - (i++ % 2) * 32;
		write(1, &temp, 1);
		//digit--;
	}
	write(1, "\n", 1);
	return (0);
}
