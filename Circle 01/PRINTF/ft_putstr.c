#include "ft_printf.h"

//string

void	ft_putstr(char *s, int *res)
{
	size_t	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		(*res) += 6;
		return ;
	}
	while (s[i])
	{
		ft_putchar(s[i], res);
		i++;
	}
}
